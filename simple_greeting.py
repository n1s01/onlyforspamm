# Расширенная программа приветствия на Python

import datetime
import random

def get_time_of_day():
    """Определяет время суток"""
    current_hour = datetime.datetime.now().hour
    
    if 5 <= current_hour < 12:
        return "утро"
    elif 12 <= current_hour < 17:
        return "день"
    elif 17 <= current_hour < 22:
        return "вечер"
    else:
        return "ночь"

def get_random_fact():
    """Возвращает случайный интересный факт"""
    facts = [
        "Питон был назван в честь комедийного шоу 'Летающий цирк Монти Пайтона'.",
        "Python был создан Гвидо ван Россумом в 1991 году.",
        "В Python есть встроенная функция для игры в 'Камень, ножницы, бумага' - это шутка!",
        "Python используется в NASA, Google, Facebook и многих других крупных компаниях.",
        "Название 'Python' происходит не от змеи, а от комедийного шоу.",
        "Python поддерживает более 100 000 пакетов в репозитории PyPI.",
        "Python - один из самых быстрых растущих языков программирования в мире."
    ]
    return random.choice(facts)

# Запрашиваем имя пользователя
name = input("Как вас зовут? ")

# Определяем время суток
time_of_day = get_time_of_day()

# Выводим персональное приветствие с учётом времени суток
print(f"Доброе {time_of_day}, {name}! Добро пожаловать в мир Python!")

# Добавляем случайный факт о Python
print("\nИнтересный факт о Python:")
print(get_random_fact())

# Спрашиваем, хочет ли пользователь узнать ещё что-то
more_info = input("\nХотите узнать ещё один факт? (да/нет): ").lower()
if more_info == "да":
    print(get_random_fact())

print("\nЭто расширенная программа на Python, которая не только приветствует вас, но и делится интересными фактами!")
